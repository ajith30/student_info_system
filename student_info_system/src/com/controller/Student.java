package com.controller;

public class Student {

	private String studentName;
	private int studentRollNo;
	private String studentEmailId;
	private Long studentPhoneNo;
	private String studentAddress;
	private int studentSemester;
	private String studentDepartment;
	private String studentRemarks;
	
	
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public int getStudentRollNo() {
		return studentRollNo;
	}
	public void setStudentRollNo(int studentRollNo) {
		this.studentRollNo = studentRollNo;
	}
	public String getStudentEmailId() {
		return studentEmailId;
	}
	public void setStudentEmailId(String studentEmailId) {
		this.studentEmailId = studentEmailId;
	}
	public Long getStudentPhoneNo() {
		return studentPhoneNo;
	}
	public void setStudentPhoneNo(Long studentPhoneNo) {
		this.studentPhoneNo = studentPhoneNo;
	}
	public String getStudentAddress() {
		return studentAddress;
	}
	public void setStudentAddress(String studentAddress) {
		this.studentAddress = studentAddress;
	}
	public int getStudentSemester() {
		return studentSemester;
	}
	public void setStudentSemester(int studentSemester) {
		this.studentSemester = studentSemester;
	}
	public String getStudentDepartment() {
		return studentDepartment;
	}
	public void setStudentDepartment(String studentDepartment) {
		this.studentDepartment = studentDepartment;
	}
	public String getStudentRemarks() {
		return studentRemarks;
	}
	public void setStudentRemarks(String studentRemarks) {
		this.studentRemarks = studentRemarks;
	}
	
	

	
	
}
